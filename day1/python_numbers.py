# There are three numeric types in Python: int, float and complex

# int type: int are whole numbers either positive or negative 
int_value1 = 563
int_value2 = 10
print(int_value1)
print(int_value2)
print(type(int_value1))
print(type(int_value2))

# float type: floats are numbers with decimals either positive or negative
float_value1 = 9.28
float_value2 = 30.9678e10 # e means exponential increase the decimal point
print(float_value1)
print(float_value2)
print(type(float_value1))
print(type(float_value2))

#complex: are numers written with a "j" as the imaginary part
complex_value1 = 3 + 7j
complex_value2 = 8j
print(complex_value1) #cannot 
print(complex_value2)
print(type(complex_value1))
print(type(complex_value2))